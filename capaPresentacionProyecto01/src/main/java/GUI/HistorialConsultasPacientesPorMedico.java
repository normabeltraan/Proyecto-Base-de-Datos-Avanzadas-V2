    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nombrePaciente = txtNombre.getText().trim();
        String nombreCompletoMedico = String.format("%s %s %s",
                medico.getNombre(),
                medico.getApellido_paterno(),
                medico.getApellido_materno() != null ? medico.getApellido_materno() : ""
        ).trim();
        if (!nombrePaciente.isEmpty()) {

            cargarHistorialConsultas(nombrePaciente, nombreCompletoMedico);
        } else {
            DefaultTableModel tabla = (DefaultTableModel) tablaConsultas.getModel();
            tabla.setRowCount(0);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


