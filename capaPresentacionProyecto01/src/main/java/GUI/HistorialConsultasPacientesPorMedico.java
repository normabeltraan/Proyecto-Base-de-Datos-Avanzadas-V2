    private void cargarHistorialConsultas(String nombrePaciente, String nombreMedico) {
        try {

            List<ConsultaDTO> historial = pacienteBO.obtenerHistorialConsultasDelPacientePorMedico(nombrePaciente, nombreMedico);
            System.out.println(historial);
            DefaultTableModel tabla = (DefaultTableModel) tablaConsultas.getModel();
            tabla.setRowCount(0);

            for (ConsultaDTO consulta : historial) {
                Object[] row = new Object[]{
                    consulta.getCita().getFecha_hora(),
                    consulta.getCita().getTipo(),
                    consulta.getCita().getEstado(),
                    consulta.getTratamiento(),
                    consulta.getDiagnostico()
                };
                tabla.addRow(row);
            }
        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }
